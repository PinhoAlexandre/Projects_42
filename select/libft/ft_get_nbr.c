#include "libft.h"

int		get_nb(const char *str, int *i)
{
	int	ret;

	ret = 0;
	if (str[*i] == '.')
		*i += 1;
	while (str[*i] >= '0' && str[*i] <= '9')
	{
		ret *= 10;
		ret += str[*i] - '0';
		*i += 1;
	}
	return (ret);
}
