#include "libft.h"

char	*ft_strcat(char *s1, char const *s2)
{
	int i;
	int x;

	i = 0;
	x = 0;
	while (s1[i])
		i++;
	while (s2[x])
	{
		s1[i] = s2[x];
		i++;
		x++;
	}
	s1[i] = '\0';
	return (s1);
}
